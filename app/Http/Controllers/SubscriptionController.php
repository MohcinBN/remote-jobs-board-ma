    {
        //
       $request->validate([
        'email' => 'required',
        ]);

        $subscription = new Subscription;
        $subscription->email = trim($request->email);

        $subscription->save();

        return view('welcome')->with('status',' Subscribed! Thank you :) ');
        

        // return json for API using..
        return response()->json('Subscribed! Thank you');
    }

